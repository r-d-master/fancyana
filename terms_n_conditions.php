When do you need an alteration?<br>

An alteration is either size or design related.<br>

Size Alteration: If there's been an issue with your sizing, we will always work with you to get it 

right. Every time.<br>

Design Alteration: If you require a design change (ex. collar, cuffs, buttons) after receiving your 

order, we'll try and incorporate the changes as far as possible. <br>

We do not charge anything for alterations.<br>

How do you go about an alteration?<br>

Alterations are not so desirable, yet, at times, unavoidable. We'll try to make it as pain free as 

possible. Help us help you, one step at a time.<br>

If you've ordered through our website, send the  dress back to our office addresses mentioned 

on website or request a pick up- our stylists / master tailors will take a look and suggest 

necessary changes. For most alterations, our master tailors will have them completed within10 

days.<br>

If you need to send your  dress back for alteration, kindly do so while referencing your name 

and order number, and send that to our address written in the website.<br>

Once the package reaches us, we'll take care of the rest. All requests take 10 working days from 

the date of receipt. We know you're keen to have your dress back; we'll do our best to get it to 

you sooner.<br>

For international customers, we offer alteration credit on your first order. Take the dress in 

question to a local tailor for alteration, we will reimburse up to 300 loyalty points per dress (or 

roughly $5) on display of a valid receipt. Alternatively, if you'd like for us to alter the dress for 

you, kindly send it back the address mentioned on the website. Return shipping fees will be 

applicable, however.<br>

